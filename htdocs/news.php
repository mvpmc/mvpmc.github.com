<h3>New Release - 9-Sep-04</h3>

<p>
A new version of mvpmc has been
released <a href="idx.php?pg=downloads">here</a>.  New
features include:
</p>

<ul>
<li>Support for uncompressed WAV audio files.
<li>Support for AC3 audio files.
<li>Support for AC3 audio streams in mpeg files (ie, DVD VOB files).
<li>Ability to choose between multiple audio and video streams in a file.
<li>Ability to play files via the MythTV protocol.
<li>Popup menu available during video playback.
<li>Improved On-Screen-Display.
</ul>
